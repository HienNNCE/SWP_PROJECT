    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();

        String myOTP1 = request.getParameter("otp1");
        String myOTP2 = request.getParameter("otp2");
        String myOTP3 = request.getParameter("otp3");
        String myOTP4 = request.getParameter("otp4");
        String myOTP5 = request.getParameter("otp5");

        // Ghép các ký tự OTP lại
        String inputOTP = myOTP1 + myOTP2 + myOTP3 + myOTP4 + myOTP5;

        String sessionOTP = (String) session.getAttribute("otp");

        if (inputOTP.equalsIgnoreCase(sessionOTP)) {
            
            session.setAttribute("otp_verified", true);

            
            session.removeAttribute("otp");

            
            request.getRequestDispatcher("resetPassword.jsp").forward(request, response);
        } else {
            
            request.setAttribute("err", "OTP code is incorrect. Please try again.");
            request.getRequestDispatcher("OTP.jsp").forward(request, response);
        }
    }

